#include "3-calc.h"

/**
 * op_add - function to add
 * @a: first arg
 * @b: second arg
 *
 * Return: Sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function to sub
 * @a: first arg
 * @b: second arg
 *
 * Return: Sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function to mul
 * @a: first arg
 * @b: second arg
 *
 * Return: Mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function to div
 * @a: first arg
 * @b: second arg
 *
 * Return: Div
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function to mod
 * @a: first arg
 * @b: second arg
 *
 * Return: Mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
